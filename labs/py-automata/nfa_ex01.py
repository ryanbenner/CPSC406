import nfa as nfa

def __main__():
    # A1:
    Q1 = {'q0', 'q1'}
    Sigma1 = {'0', '1'}
    delta1 = {
        ('q0', '0'): {'q0', 'q1'},
        ('q1', '1'): {'q0'},
    }
    q0_1 = 'q0'
    F1 = {'q1'}

    # A2:
    Q2 = {'q0', 'q1', 'q2'}
    Sigma2 = {'0', '1'}
    delta2 = {
        ('q0', '0'): {'q0', 'q1'},
        ('q0', '1'): {'q0'},
        ('q1', '1'): {'q2'},
        ('q2', '0'): {'q2'},
        ('q2', '1'): {'q2'}
    }
    q0_2 = 'q0'
    F2 = {'q2'}

    # A3:
    Q3 = {'q0', 'q1', 'q2'}
    Sigma3 = {'0', '1'}
    delta3 = {
        ('q0', '0'): {'q0', 'q1'},
        ('q0', '1'): {'q0'},
        ('q1', '1'): {'q2'},
        ('q2', '0'): {'q2'},
        ('q2', '1'): {'q2'}
    }
    q0_3 = 'q0'
    F3 = {'q0', 'q1'}

    # A4:
    Q4 = {'q0', 'q1', 'q2', 'q3', 'q4'}
    Sigma4 = {'0', '1'}
    delta4 = {
        ('q0', '0'): {'q0'},
        ('q0', '1'): {'q0', 'q1'},
        ('q1', '0'): {'q2'},
        ('q1', '1'): {'q2'},
        ('q2', '0'): {'q3'},
        ('q2', '1'): {'q3'},
        ('q3', '0'): {'q4'},
        ('q3', '1'): {'q4'},
        ('q4', '0'): {'q4'},
        ('q4', '1'): {'q4'},
    }
    q0_4 = 'q0'
    F4 = {'q4'}

    A1 = nfa.NFA(Q1, Sigma1, delta1, q0_1, F1)
    A2 = nfa.NFA(Q2, Sigma2, delta2, q0_2, F2)
    A3 = nfa.NFA(Q3, Sigma3, delta3, q0_3, F3)
    A4 = nfa.NFA(Q4, Sigma4, delta4, q0_4, F4)

    words = ['00', '01', '010', '001', '100', '1001', '01011',' 1000', '010010']
    automata = [A1]

    for X in automata:
        print(f"{X.__repr__()}")
        for w in words:
            print(f"{w}: {X.run(w)}")
        print("\n")
    
__main__()